// For example:
//
//   import SyntaxHighlighting from "ls-plugin-syntax-highlighting";
//   import GhostWriter from "ls-plugin-ghost-writer";
//
//    export default {
//       SyntaxHighlighting,
//       GhostWriter,
//    };
//
// For this to work, the plugins don't need to be included in the source
// code of the core application. Instead it is recommended to create an own
// npm module for each plugin. This in turn means, you have to "npm install -D"
